										<p style='font-size:20px;'>
                                        The pulmonary circuit is the path of circulation between the heart and the lungs. Blood is pumped to the various places of the body by a process known as the cardiac cycle. Oxygen depleted blood returns from the body to the right atrium of the heart by two large veins called vena cavae. Electrical impulses produced by cardiac conduction cause the heart to contract. As a result, blood in the right atrium is pumped to the right ventricle.

On the next heart beat, the contraction of the right ventricle sends the oxygen-depleted blood to the lungs via the pulmonary artery. This artery branches into left and right pulmonary arteries. In the lungs, carbon dioxide in the blood is exchanged for oxygen at lung alveoli. Alveoli are small air sacs that are coated with a moist film that dissolves air. As a result, gases can diffuse across the thin endothelium of the alveoli sacs.

The now oxygen-rich blood is transported back to the heart by the pulmonary veins. The pulmonary circuit is completed when pulmonary veins return blood to the left atrium of the heart. When the heart contracts again, this blood is pumped from the left atrium to the left ventricle and later to systemic circulation.
