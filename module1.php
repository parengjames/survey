                                        <p align="justify" style='font-size:25px;'>
                                        The middle and thickest layer is the <b style="color:blue"> myocardium, </b>  made largely of cardiac muscle cells. It is built upon a framework of fibers, plus the blood vessels that supply the myocardium and the nerve fibers that help regulate the heart. It is the contraction of the myocardium that pumps blood through the heart and into the major arteries.
                                        </p>
                                        </div>
                                        </div>

                                        <br>
