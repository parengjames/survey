                                        The  <a href="heart.php" style='font-size:25px;' ><b style="color:blue"> heart</a></b> is a muscular <b style="color:blue"> pump </a></b> that provides the force necessary to circulate the <a href="blood.php" style='font-size:20px;' >  <b style="color:blue"> blood </a></b>  to all the tissues in the body. Its function is vital because, to survive, the tissues need a continuous supply of <a href="oxygen.php" style='font-size:20px;' > <b style="color:blue"> oxygen </a> </b> and nutrients, and <a href="metabolic.php" style='font-size:20px;' ><b style="color:blue"> metabolic </a> </b>waste products have to be removed. Deprived of these necessities, cells soon undergo irreversible changes that lead to death. While blood is the transport medium, the heart is the <a href="organ.php" style='font-size:20px;' ><b style="color:blue"> organ </a></b> that keeps the blood moving through the vessels. The normal adult heart pumps about 5 liters of blood every minute throughout life. If it loses its pumping <b style="color:blue"> effectiveness  </b> for even a few minutes, the individual's life is jeopardized.
