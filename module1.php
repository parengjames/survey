                                    <p align="justify" style='font-size:25px;'>
                                    The human heart consists of four chambers: The left side and the right side each have one <b style="color:blue">atrium </b> and one <b style="color:blue">ventricle. </b> Each of the upper chambers, the right atrium (plural = atria) and the left atrium, acts as <b style="color:blue">a receiving chamber </b> and contracts to push blood into the lower chambers, the right ventricle and the left ventricle. The ventricles serve as the <b style="color:blue"> primary pumping chambers</b> of the heart, propelling blood to the lungs or to the rest of the body.</p>
                                    </div>
