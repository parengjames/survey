                                        Although the ventricles on the right and left sides pump the same amount of blood per contraction, the muscle of the <b style="color:blue"> left ventricle </b>  is much thicker and better developed than that of the right ventricle. In order to overcome the high resistance required to pump blood into the long body circuit, the left ventricle must generate a great amount of pressure. The right ventricle does not need to generate as much pressure, since the route to and from the lungs is shorter and provides less resistance. [link] illustrates the differences in muscular thickness needed for each of the ventricles.
                                        </p>
