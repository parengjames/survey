                                    <p align="justify" style='font-size:25px;'>
                                    There are two distinct but linked circuits in the human circulation called the  pulmonary and systemic circuits. The <a href="pulmonarycircuit.php" style='font-size:20px;' > <b style="color: blue;">pulmonary circuit</a></b> transports blood to and from the lungs, where it picks up oxygen and delivers carbon dioxide for exhalation. The <a href="systemcircuit.php" style='font-size:20px;' > <b style="color: blue;">system circuit</a></b> transports oxygenated blood to virtually all of the tissues of the body and returns to the heart.</p>
                                    </div>
								</div>
                                    <br>
                                    <div class="col-lg-12 col-md-12 col-sm-12" >
									<div class="product-detail-desc pd-30 card-box height-100-p" style="background-color:lavender"  >
                                    
                                    <p  align="justify" style='font-size:25px;'>
                                    The right ventricle pumps deoxygenated blood into the <a href="pulmonarytrunk.php" style='font-size:20px;' ><b style="color:blue"> pulmonary trunk,</a> </b> which leads toward the lungs and splits into the left and right <a href="pulmonaryarteries.php" style='font-size:20px;' > <b style="color:blue"> pulmonary arteries. </a></b> These vessels in turn branch many times before reaching the <a href="pulmonarycapillaries.php" style='font-size:20px;' ><b style="color:blue"> pulmonary capillaries, </a></b> where gas exchange occurs: Carbon dioxide exits the blood and oxygen enters. The pulmonary trunk arteries and their branches are the only arteries in the body that carry relatively deoxygenated blood. Highly oxygenated blood returning from the pulmonary capillaries in the lungs passes through a series of vessels that join together to form the <a href="pulmonaryveins.php" style='font-size:20px;' ><b style="color:blue"> pulmonary veins— </a></b> the only veins in the body that carry highly oxygenated blood. The pulmonary veins bring blood into the left atrium, which delivers the blood into the left ventricle, which in turn pumps oxygenated blood into the aorta and on to the many branches of the systemic circuit. Eventually, these vessels will lead to the systemic capillaries, where exchange with the tissue fluid and cells of the body occurs. In this case, oxygen and nutrients exit the systemic capillaries to be used by the cells in their metabolic processes, and carbon dioxide and waste products will enter the blood.</p>
