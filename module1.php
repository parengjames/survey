                                        <p align="justify" style='font-size:25px;'>
                                        The wall of the heart is composed of three layers of unequal thickness. From superficial to deep, these are the <b style="color:blue"> epicardium, </b> <b style="color:blue"> the myocardium,  </b> <b style="color:blue"> and the endocardium </b> (see Figure). The outermost layer of the wall of the heart is also the innermost layer of the pericardium, the epicardium, or the visceral pericardium discussed earlier.
