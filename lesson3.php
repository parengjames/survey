                                        <h4 class="mb-20 pt-20"><b>Layers of the Heart Wall </b></h4>
										</p>
                                        <p  style='font-size:20px;'>
                                        <b>The Epicardium.</b> The epicardium is also sometimes considered a part of the protective pericardial membrane around the heart. It helps to keep the heart lubricated and protected.
                                        </p>
                                        <p  style='font-size:20px;'>
                                       <b> The Myocardium. </b>The myocardium is the muscle of the heart. You can remember this because the root word “myo” comes from “muscle,” while “cardium” comes from “heart.”
                                        The myocardium is an incredibly strong muscle that makes up most of the heart. It is responsible for pumping blood throughout the body.
                                        </p>
                                        <p  style='font-size:20px;'>
                                        <b>The Endocardium. </b> The endocardium is a thin, protective layer on the inside of the heart. It is made of smooth, slippery endothelial cells, which prevent blood from sticking to the inside of the heart and forming deadly blood clots.
                                        </p>
